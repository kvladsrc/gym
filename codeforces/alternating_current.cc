int main(int /*argc*/, char* /*argv*/[]) {
  std::ios::sync_with_stdio(false);
  std::cin.tie(nullptr);

  string s;
  std::cin >> s;

  vector<char> st;
  for (auto c : s) {
    if (st.empty() || st.back() != c) {
      st.push_back(c);
    } else {
      st.pop_back();
    }
  }

  std::cout << (st.empty() ? "YES" : "NO") << "\n";

  return 0;
}
