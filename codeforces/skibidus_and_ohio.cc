int main(int /*argc*/, char* /*argv*/[]) {
  std::ios::sync_with_stdio(false);
  std::cin.tie(nullptr);

  int t = 0;
  std::cin >> t;
  while ((t--) != 0) {
    string s;
    std::cin >> s;

    bool res = false;
    for (size_t i = 1; i < s.size(); ++i) {
      if (s[i] == s[i - 1]) {
        res = true;
        break;
      }
    }

    std::cout << (res ? 1 : s.size()) << "\n";
  }

  return 0;
}
