int main(int argc, char *argv[]) {
  int t;
  std::cin >> t;
  while (t--) {
    int64_t r, b, d;
    std::cin >> r >> b >> d;

    if (r < b) {
      int64_t buf = r;
      r = b;
      b = buf;
    }

    if (b + (b * d) >= r) {
      std::cout << "YES"
                << "\n";
    } else {
      std::cout << "NO"
                << "\n";
    }
  }

  return 0;
}
