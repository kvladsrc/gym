const int modulo = 1e9 + 7;

int main(int argc, char *argv[]) {
  int t;
  std::cin >> t;
  while (t--) {
    int n, k;
    std::cin >> n >> k;

    long long ans = 1;
    for (int i = 0; i < k; ++i) {
      ans = (ans * n) % modulo;
    }

    std::cout << ans << "\n";
  }

  return 0;
}
