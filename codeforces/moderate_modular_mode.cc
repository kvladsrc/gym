int main(int /*argc*/, char* /*argv*/[]) {
  std::ios::sync_with_stdio(false);
  std::cin.tie(nullptr);

  int t = 0;
  std::cin >> t;
  while ((t--) != 0) {
    int64_t x = 0;
    int64_t y = 0;
    std::cin >> x >> y;

    if (x == y) {
      std::cout << x << "\n";
      continue;
    }

    if (x > y) {
      std::cout << x + y << "\n";
      continue;
    }

    std::cout << y - ((y % x) / 2) << "\n";
  }

  return 0;
}
